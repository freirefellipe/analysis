elenco = [34, 62, 23, 78]
print(elenco)

elenco.insert(1, 77) # insert: insere valor, escolhendo a posição em que ficará, determinando o índice. Insere 77 na posição 1, SEM substituição.
print(elenco)

elenco.append(92) # append: insere mais um valor/elemento no final da lista.
print(elenco)

elenco.remove(23) # remove: remove um valor/ítem digitando literalmente aquele valor.
print(elenco)

elenco.pop(3) # pop: remove um elemento da lista apontado pelo índice.
print(elenco)

elenco[2] = 11 # substitui um valor por outro de acordo com o índice dado.
print(elenco)



